//color based on type of landmark
function selectMarkerColor(category){
  if (category === 'park') {
    return greenIcon
  } else if (category ==='theater') {
    return violetIcon
  } else if (category ==='government'){
    return greyIcon
  } else {
    return orangeIcon
  }
};

